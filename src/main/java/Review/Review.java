package Review;

public class Review {
	
	private String review;
	
	
	public Review() {
		this.review = "";
	}
	
	public Review(String review) {
		this.review = review;
	}

	public String getReview() {
		return review;
	}

	public void setReview(String review) {
		this.review = review;
	}
	
	
}
